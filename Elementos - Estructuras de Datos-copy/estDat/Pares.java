package estDat;

public class Pares{
    private int p1;
    private int p2;
    
    public Pares(int p1, int p2){
        this.p1 = p1;
        this.p2 = p2;
    }
    
    public int getP1(){
        return p1;
    }
    
    public int getP2(){
        return p2;
    }
    
    public void setP1(int nP1){
        p1 = nP1;
    }
    
    public void setP2(int nP2){
        p2 = nP2;
    }
}